// Generates src/registry/palette.generated.ts from the CSS package's primitive
// $palette ramps (SCSS-only — they compile into semantic tokens and aren't
// exposed as CSS vars), so the docs can show a Storybook-style colour palette
// straight from the source of truth.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '../../..');
const src = join(repoRoot, 'packages/css/src/styles/tokens/_primitives.scss');
const outDir = resolve(__dirname, '../src/registry');
const outFile = join(outDir, 'palette.generated.ts');

const scss = readFileSync(src, 'utf8');

/** Balanced (...) block starting at the first `$palette:`. */
function paletteBlock(text) {
  const start = text.indexOf('$palette:');
  const open = text.indexOf('(', start);
  let depth = 0;
  for (let i = open; i < text.length; i++) {
    if (text[i] === '(') depth++;
    else if (text[i] === ')') {
      depth--;
      if (depth === 0) return text.slice(open, i + 1);
    }
  }
  return '';
}

// Ramp → semantic role, mirroring $_palette-roles in css/abstracts/_mixins.scss
// (the CSS vars are emitted as --lily-color-<role>-<step>).
const ROLE = {
  brand: 'primary',
  neutral: 'neutral',
  fuji: 'accent',
  sakura: 'sakura',
  red: 'danger',
  amber: 'warning',
  green: 'success',
  blue: 'info',
};

const block = paletteBlock(scss);

// Match each `'name': ( ... )` ramp.
const ramps = [];
const rampRe = /'([\w-]+)'\s*:\s*\(/g;
let m;
while ((m = rampRe.exec(block))) {
  const name = m[1];
  // capture this ramp's balanced (...)
  let depth = 0;
  let body = '';
  for (let i = block.indexOf('(', m.index); i < block.length; i++) {
    const c = block[i];
    if (c === '(') depth++;
    body += c;
    if (c === ')') {
      depth--;
      if (depth === 0) break;
    }
  }
  const role = ROLE[name] ?? name;
  const steps = [];
  const stepRe = /(\d+)\s*:\s*(#[0-9a-fA-F]{3,8})/g;
  let sm;
  while ((sm = stepRe.exec(body))) {
    const step = Number(sm[1]);
    steps.push({ step, hex: sm[2], var: `--lily-color-${role}-${step}` });
  }
  if (steps.length) ramps.push({ name, role, steps });
}

const header = `// AUTO-GENERATED by scripts/gen-palette.mjs — do not edit by hand.
// Source: packages/css/src/styles/tokens/_primitives.scss ($palette)
// Run \`pnpm --filter @lily-ui/docs gen\` to regenerate.

export interface PaletteStep {
  step: number;
  hex: string;
  /** The public CSS custom property, e.g. \`--lily-color-primary-100\`. */
  var: string;
}
export interface PaletteRamp {
  name: string;
  role: string;
  steps: PaletteStep[];
}

export const palette: PaletteRamp[] = ${JSON.stringify(ramps, null, 2)};
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, header);
console.log(`gen-palette: ${ramps.length} ramps → ${outFile.replace(repoRoot + '/', '')}`);
